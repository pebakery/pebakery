using Microsoft.VisualStudio.TestTools.UnitTesting;
using System;
using System.IO;
using System.Text;

namespace PEBakery.Helper.Tests
{
    [TestClass]
    public class HtmlHelperTests
    {
        #region GenerateDataUri
        [TestMethod]
        [TestCategory("Helper")]
        [TestCategory("HtmlHelper")]
        public void GenerateDataUriByte()
        {
            string srcDir = Path.Combine(TestSetup.SampleDir, "HtmlHelper");
            string srcFile = Path.Combine(srcDir, "Banner.png");
            string compFile = Path.Combine(srcDir, "BannerPngDataUri.txt");

            string destDataUri;
            using (FileStream fs = new FileStream(srcFile, FileMode.Open, FileAccess.Read))
            {
                byte[] buffer = new byte[fs.Length];
                fs.Read(buffer, 0, buffer.Length);
                destDataUri = HtmlHelper.GenerateDataUri(HtmlHelper.MimeType.Png, buffer);
            }

            using (StreamReader r = new StreamReader(compFile, Encoding.UTF8, false))
            {
                string compDataUri = r.ReadToEnd();
                Assert.IsTrue(destDataUri.Equals(compDataUri, StringComparison.Ordinal));
            }
        }

        [TestMethod]
        [TestCategory("Helper")]
        [TestCategory("HtmlHelper")]
        public void GenerateDataUriStr()
        {
            string srcDir = Path.Combine(TestSetup.SampleDir, "HtmlHelper");
            string srcFile = Path.Combine(srcDir, "BannerDist.svg");
            // CompFile generated by https://dopiaza.org/tools/datauri/index.php
            // Unable to test urlencoded data uri, because implementations generate different results.
            string compBase64File = Path.Combine(srcDir, "BannerDistSvgBase64DataUri.txt");

            string base64DataUri;
            using (StreamReader r = new StreamReader(srcFile, Encoding.UTF8, false))
            {
                string srcFileData = r.ReadToEnd();
                base64DataUri = HtmlHelper.GenerateDataUri(HtmlHelper.MimeType.Svg, srcFileData, true);
            }

            using (StreamReader r = new StreamReader(compBase64File, Encoding.UTF8, false))
            {
                string compDataUri = r.ReadToEnd();
                Assert.IsTrue(base64DataUri.Equals(compDataUri, StringComparison.Ordinal));
            }
        }
        #endregion
    }
}
